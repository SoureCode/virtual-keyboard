export const withAlternates = (key: {
  label: string;
  action: { kind: "char"; value: string };
}): {
  label: string;
  action: { kind: "char"; value: string };
  alternates?: string[];
} => {
  const alts = alternates[key.action.value.toLowerCase()];
  return alts ? { ...key, alternates: alts } : key;
};
